_EMPTY_PROFILE_NOTE = (
    "Контекст участника: профиль не заполнен. "
    "Не выдумывай факты о его бизнесе — опирайся только на то, что он говорит сейчас."
)


def build_profile_context(profile_json: str) -> str:
    """Компактный блок «Контекст участника» для подстановки в промпты."""
    if not profile_json or profile_json.strip() in ("", "{}"):
        return _EMPTY_PROFILE_NOTE

    try:
        data = json.loads(profile_json)
    except json.JSONDecodeError:
        return _EMPTY_PROFILE_NOTE

    if not isinstance(data, dict):
        return _EMPTY_PROFILE_NOTE

    business = data.get("business") or {}
    economics = data.get("economics") or {}
    goals = data.get("goals") or {}
    style = data.get("style") or {}

    lines = ["Контекст участника:"]
    if business.get("niche"):
        lines.append(f"- Ниша: {business['niche']}")
    if business.get("product"):
        lines.append(f"- Продукт/услуга: {business['product']}")
    if business.get("stage"):
        lines.append(f"- Стадия: {business['stage']}")
    if economics.get("bottleneck"):
        lines.append(f"- Узкое место: {economics['bottleneck']}")
    if goals.get("year_goal"):
        lines.append(f"- Годовая цель: {goals['year_goal']}")
    if goals.get("quarter_goal"):
        lines.append(f"- Квартальная цель: {goals['quarter_goal']}")
    if goals.get("success_criteria"):
        lines.append(f"- Критерий успеха: {goals['success_criteria']}")
    if style.get("hours_per_week"):
        lines.append(f"- Часов в неделю на развитие: {style['hours_per_week']}")

    if len(lines) == 1:
        return _EMPTY_PROFILE_NOTE
    return "\n".join(lines)
